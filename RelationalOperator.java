public class RelationalOperator {
    public static void main(String args[]){
        int a = 10;
        int b = 5;
        System.out.println("Is the value of a and b are equal: " + (a==b));
        System.out.println("Is the value of a and b are NOT equal: " + (a!=b));
        System.out.println("Is the value of a is greater than b: " + (a>b));
        System.out.println("Is the value of a is less than b: " + (a<b));
    }
}
